#define OUTPUT_STRING_LEN 256

//=================================================================================================================//

Token * gCurrentToken = NULL;

//=================================================================================================================//

Token::Token( TokenID tid, const std::string & data) : mId( tid ), mData( data )
{

}

//=================================================================================================================//

Token::~Token()
{

}

//=================================================================================================================//

std::string Token::print() const
{
	char str[OUTPUT_STRING_LEN];
	snprintf( str, OUTPUT_STRING_LEN, "Typ:    %14s \t\t data:  '%s'", TokenNames[ mId ], mData.c_str() );
	return str;
}

//=================================================================================================================//

TokenID Token::getID() const
{
	return mId;
}

//=================================================================================================================//

const std::string & Token::getData() const
{
	return mData;
}

